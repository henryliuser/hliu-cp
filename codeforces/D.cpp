#include <bits/stdc++.h>
using namespace std;
using ll = long long;

ll solve() {
    int N; cin >> N;
    vector<string> A(N);
    for (auto &s : A) cin >> s;

    for (int i = 0; i < N; ++i) {
        string s = A[i];
    }
}

int main() {
    int t; cin >> t;
    while (t--)
        cout << solve() << '\n';
}
